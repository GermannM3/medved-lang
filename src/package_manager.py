#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Менеджер пакетов для языка Медведь
Позволяет устанавливать, удалять и управлять библиотеками
"""

import os
import sys
import json
import shutil
import urllib.request
import tarfile
from pathlib import Path

class МенеджерПакетов:
    """Управление пакетами для языка Медведь"""
    
    def __init__(self):
        self.корень_домой = Path.home() / '.медведь'
        self.папка_пакетов = self.корень_домой / 'пакеты'
        self.файл_реестра = self.корень_домой / 'реестр.json'
        self.реестр = self._загрузить_реестр()
        
        # Создаём директории если не существуют
        self.папка_пакетов.mkdir(parents=True, exist_ok=True)
    
    def _загрузить_реестр(self):
        """Загрузить реестр установленных пакетов"""
        if self.файл_реестра.exists():
            with open(self.файл_реестра, 'r', encoding='utf-8') as f:
                return json.load(f)
        return {'пакеты': {}}
    
    def _сохранить_реестр(self):
        """Сохранить реестр установленных пакетов"""
        with open(self.файл_реестра, 'w', encoding='utf-8') as f:
            json.dump(self.реестр, f, ensure_ascii=False, indent=2)
    
    def установить(self, имя_пакета, версия=None):
        """Установить пакет из репозитория"""
        print(f"Установка пакета '{имя_пакета}'...")
        
        # Проверяем локальную папку packages
        локальный_путь = Path('packages') / имя_пакета
        if локальный_путь.exists():
            return self._установить_локальный(локальный_путь, имя_пакета)
        
        # Проверяем глобальный реестр пакетов Медведь
        if имя_пакета in self._получить_список_пакетов():
            return self._установить_из_реестра(имя_пакета, версия)
        
        # Пробуем установить как Python пакет (для совместимости)
        try:
            import subprocess
            subprocess.check_call([sys.executable, '-m', 'pip', 'install', имя_пакета])
            print(f"Пакет '{имя_пакета}' установлен как Python пакет")
            return True
        except Exception as e:
            print(f"Ошибка установки пакета: {e}")
            return False
    
    def _установить_локальный(self, путь, имя):
        """Установить пакет из локальной директории"""
        print(f"Установка локального пакета из {путь}...")
        
        dest = self.папка_пакетов / имя
        if dest.exists():
            shutil.rmtree(dest)
        
        shutil.copytree(путь, dest)
        
        self.реестр['пакеты'][имя] = {
            'версия': '0.0.0',
            'путь': str(dest),
            'тип': 'локальный'
        }
        self._сохранить_реестр()
        
        print(f"✓ Пакет '{имя}' успешно установлен")
        return True
    
    def _установить_из_реестра(self, имя, версия):
        """Установить пакет из официального реестра"""
        # Заглушка для будущей реализации
        print(f"Пакет '{имя}' будет доступен в будущем реестре")
        return False
    
    def удалить(self, имя_пакета):
        """Удалить установленный пакет"""
        if имя_пакета not in self.реестр['пакеты']:
            print(f"Пакет '{имя_пакета}' не найден")
            return False
        
        пакет = self.реестр['пакеты'][имя_пакета]
        путь = Path(пакет['путь'])
        
        if путь.exists():
            shutil.rmtree(путь)
        
        del self.реестр['пакеты'][имя_пакета]
        self._сохранить_реестр()
        
        print(f"✓ Пакет '{имя_пакета}' удалён")
        return True
    
    def список(self):
        """Показать список установленных пакетов"""
        if not self.реестр['пакеты']:
            print("Нет установленных пакетов")
            return
        
        print("\nУстановленные пакеты:")
        print("-" * 50)
        for имя, информация in self.реестр['пакеты'].items():
            print(f"  {имя} v{информация['версия']} ({информация['тип']})")
        print("-" * 50)
    
    def _получить_список_пакетов(self):
        """Получить список доступных пакетов из реестра"""
        # Заглушка - в будущем будет запрос к серверу
        return ['математика', 'строки', 'даты', 'json', 'http']
    
    def создать(self, имя_пакета):
        """Создать шаблон нового пакета"""
        print(f"Создание пакета '{имя_пакета}'...")
        
        папка = Path(имя_пакета)
        if папка.exists():
            print(f"Ошибка: директория '{имя_пакета}' уже существует")
            return False
        
        папка.mkdir()
        
        # Создаём структуру пакета
        (папка / 'src').mkdir()
        (папка / 'tests').mkdir()
        (папка / 'docs').mkdir()
        
        # Создаём файл описания пакета
        описание = {
            'имя': имя_пакета,
            'версия': '0.1.0',
            'описание': 'Описание пакета',
            'автор': 'Ваше имя',
            'лицензия': 'MIT',
            'файлы': ['*.мед']
        }
        
        with open(папка / 'пакет.json', 'w', encoding='utf-8') as f:
            json.dump(описание, f, ensure_ascii=False, indent=2)
        
        # Создаём README
        readme = f"""# {имя_пакета}

Описание пакета.

## Установка

```bash
медведь-пакет установить {имя_пакета}
```

## Использование

```медведь
импорт {имя_пакета}
```
"""
        
        with open(папка / 'README.md', 'w', encoding='utf-8') as f:
            f.write(readme)
        
        # Создаём пример файла
        пример = f"""# Модуль {имя_пакета}

функция привет():
    печать("Привет из пакета {имя_пакета}!")

__все__ = ['привет']
"""
        
        with open(папка / 'src' / f'{имя_пакета}.мед', 'w', encoding='utf-8') as f:
            f.write(пример)
        
        print(f"✓ Пакет '{имя_пакета}' создан в директории {папка}")
        print("  Отредактируйте пакет.json и добавьте свой код в src/")
        return True


def главная():
    """Точка входа для CLI"""
    if len(sys.argv) < 2:
        print("""
Медведь - Менеджер пакетов

Использование:
    медведь-пакет установить <пакет>     Установить пакет
    медведь-пакет удалить <пакет>        Удалить пакет
    медведь-пакет список                 Показать установленные пакеты
    медведь-пакет создать <пакет>        Создать новый пакет
    медведь-пакет помощь                 Показать эту справку

Примеры:
    медведь-пакет установить математика
    медведь-пакет создать мой_пакет
""")
        return
    
    команда = sys.argv[1]
    менеджер = МенеджерПакетов()
    
    if команда == 'установить':
        if len(sys.argv) < 3:
            print("Ошибка: укажите имя пакета")
            return
        менеджер.установить(sys.argv[2])
    
    elif команда == 'удалить':
        if len(sys.argv) < 3:
            print("Ошибка: укажите имя пакета")
            return
        менеджер.удалить(sys.argv[2])
    
    elif команда == 'список':
        менеджер.список()
    
    elif команда == 'создать':
        if len(sys.argv) < 3:
            print("Ошибка: укажите имя пакета")
            return
        менеджер.создать(sys.argv[2])
    
    elif команда in ['помощь', 'help', '-h', '--help']:
        print("""
Медведь - Менеджер пакетов

Использование:
    медведь-пакет установить <пакет>     Установить пакет
    медведь-пакет удалить <пакет>        Удалить пакет
    медведь-пакет список                 Показать установленные пакеты
    медведь-пакет создать <пакет>        Создать новый пакет
    медведь-пакет помощь                 Показать эту справку

Примеры:
    медведь-пакет установить математика
    медведь-пакет создать мой_пакет
""")
        return
    
    else:
        print(f"Неизвестная команда: {команда}")
        print("Используйте 'медведь-пакет помощь' для справки")


if __name__ == '__main__':
    главная()
