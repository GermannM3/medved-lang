# -*- coding: utf-8 -*-
from аст import *
from ошибки import ОшибкаВремениВыполнения, ОшибкаИмени, ОшибкаТипа, ОшибкаАргумента

class ВозвратИзИсключения(Exception):
    def __init__(self, значение):
        self.значение = значение

class ФункцияМедведь:
    def __init__(self, объявление, окружение):
        self.объявление = объявление
        self.окружение = окружение
    
    def вызвать(self, аргументы):
        if len(аргументы) != len(self.объявление.параметры):
            raise ОшибкаАргумента(f"Функция ожидает {len(self.объявление.параметры)} аргументов")
        новое_окружение = Окружение(родитель=self.окружение)
        for i in range(len(аргументы)):
            новое_окружение.объявить(self.объявление.параметры[i], аргументы[i])
        интерпретатор = Интерпретатор()
        try:
            интерпретатор.выполнить_блок(self.объявление.тело, новое_окружение)
        except ВозвратИзИсключения as е:
            return е.значение
        return None

class Окружение:
    def __init__(self, родитель=None):
        self.данные = {}
        self.родитель = родитель
    
    def объявить(self, имя, значение):
        self.данные[имя] = значение
    
    def присвоить(self, имя, значение):
        if имя in self.данные:
            self.данные[имя] = значение
        elif self.родитель is not None:
            self.родитель.присвоить(имя, значение)
        else:
            raise ОшибкаИмени(f"Переменная '{имя}' не определена")
    
    def получить(self, имя):
        if имя in self.данные:
            return self.данные[имя]
        elif self.родитель is not None:
            return self.родитель.получить(имя)
        else:
            raise ОшибкаИмени(f"Переменная '{имя}' не определена")

class Интерпретатор:
    def __init__(self):
        self.глобальное_окружение = Окружение()
        self._зарегистрировать_встроенные()
    
    def _зарегистрировать_встроенные(self):
        def печать_функция(*аргументы):
            строки = []
            for arg in аргументы:
                if arg is None: строки.append("ничто")
                elif isinstance(arg, bool): строки.append("истина" if arg else "ложь")
                else: строки.append(str(arg))
            print("".join(строки))
            return None
        
        def ввод_функция(подсказка=""):
            return input(подсказка)
        
        def длина_функция(объект):
            if isinstance(объект, (list, str)): return len(объект)
            raise ОшибкаТипа(f"Функция 'длина' неприменима к типу {type(объект).__name__}")
        
        def тип_функция(объект):
            if объект is None: return "ничто"
            elif isinstance(объект, (int, float)): return "число"
            elif isinstance(объект, str): return "строка"
            elif isinstance(объект, bool): return "булево"
            elif isinstance(объект, list): return "список"
            elif isinstance(объект, dict): return "словарь"
            elif isinstance(объект, ФункцияМедведь): return "функция"
            else: return "неизвестно"
        
        def преобр_число(объект):
            try: return float(объект)
            except: raise ОшибкаТипа(f"Невозможно преобразовать в число: {объект}")
        
        def преобр_строка(объект):
            if объект is None: return "ничто"
            elif isinstance(объект, bool): return "истина" if объект else "ложь"
            else: return str(объект)
        
        self.глобальное_окружение.объявить("печать", печать_функция)
        self.глобальное_окружение.объявить("ввод", ввод_функция)
        self.глобальное_окружение.объявить("длина", длина_функция)
        self.глобальное_окружение.объявить("тип", тип_функция)
        self.глобальное_окружение.объявить("преобр_число", преобр_число)
        self.глобальное_окружение.объявить("преобр_строка", преобр_строка)
    
    def выполнить(self, аст, аргументы_командной_строки=[]):
        if len(аргументы_командной_строки) > 0:
            self.глобальное_окружение.объявить("аргументы", аргументы_командной_строки)
        return self.посетить(аст, self.глобальное_окружение)
    
    def выполнить_блок(self, утверждения, окружение):
        результат = None
        for утв in утверждения:
            результат = self.посетить(утв, окружение)
        return результат
    
    def посетить(self, узел, окружение):
        метод_имя = f"посетить_{type(узел).__name__.lower()}"
        метод = getattr(self, метод_имя, None)
        if метод: return метод(узел, окружение)
        raise NotImplementedError(f"Нет обработчика для {type(узел).__name__}")
    
    def посетить_узельпрограмма(self, узел, окружение): return self.выполнить_блок(узел.утверждения, окружение)
    def посетить_узельчисло(self, узел, окружение): return узел.значение
    def посетить_узельстрока(self, узел, окружение): return узел.значение
    def посетить_узельбулево(self, узел, окружение): return узел.значение
    def посетить_узельничто(self, узел, окружение): return None
    def посетить_узельимя(self, узел, окружение): return окружение.получить(узел.имя)
    
    def посетить_узельбинарный(self, узел, окружение):
        лево = self.посетить(узел.лево, окружение)
        право = self.посетить(узел.право, окружение)
        оп = узел.оператор
        if оп == "+": return лево + право
        elif op == "-": return лево - право
        elif op == "*": return лево * право
        elif op == "/": 
            if право == 0: raise ОшибкаВремениВыполнения("Деление на ноль")
            return лево / право
        elif op == "%": return лево % право
        elif op == "**": return лево ** право
        elif op == "==": return лево == право
        elif op == "!=": return лево != право
        elif op == ">": return лево > право
        elif op == "<": return лево < право
        elif op == ">=": return лево >= право
        elif op == "<=": return лево <= право
        elif op == "и": return лево and право
        elif op == "или": return лево or право
        raise ОшибкаВремениВыполнения(f"Неизвестный оператор: {op}")
    
    def посетить_узельунарный(self, узел, окружение):
        операнд = self.посетить(узел.операнд, окружение)
        if узел.оператор == "-": return -операнд
        elif узел.оператор == "не": return not операнд
        raise ОшибкаВремениВыполнения(f"Неизвестный оператор: {узел.оператор}")
    
    def посетить_вызовфункции(self, узел, окружение):
        функция = окружение.получить(узел.имя)
        аргументы = [self.посетить(arg, окружение) for arg in узел.аргументы]
        if isinstance(функция, ФункцияМедведь): return функция.вызвать(аргументы)
        elif callable(функция): return функция(*аргументы)
        raise ОшибкаТипа(f"'{узел.имя}' не является функцией")
    
    def посетить_узельсписок(self, узел, окружение):
        return [self.посетить(e, окружение) for e in узел.элементы]
    
    def посетить_узельсловарь(self, узел, окружение):
        словарь = {}
        for k, v in узел.пары:
            словарь[self.посетить(k, окружение)] = self.посетить(v, окружение)
        return словарь
    
    def посетить_узельприсваивание(self, узел, окружение):
        значение = self.посетить(узел.значение, окружение)
        окружение.присвоить(узел.имя, значение)
        return значение
    
    def посетить_узельвыражение(self, узел, окружение):
        return self.посетить(узел.выражение, окружение)
    
    def посетить_узельесли(self, узел, окружение):
        if self.посетить(узел.условие, окружение):
            return self.выполнить_блок(узел.тогда, окружение)
        for cond, body in узел.иначе_если:
            if self.посетить(cond, окружение):
                return self.выполнить_блок(body, окружение)
        if узел.иначе is not None:
            return self.выполнить_блок(узел.иначе, окружение)
        return None
    
    def посетить_узельпока(self, узел, окружение):
        результат = None
        while self.посетить(узел.условие, окружение):
            результат = self.выполнить_блок(узел.тело, окружение)
        return результат
    
    def посетить_узельдля(self, узел, окружение):
        итерируемое = self.посетить(узел.итерируемое, окружение)
        результат = None
        for элемент in итерируемое:
            новое = Окружение(родитель=окружение)
            новое.объявить(узел.переменная, элемент)
            результат = self.выполнить_блок(узел.тело, новое)
        return результат
    
    def посетить_узельфункция(self, узел, окружение):
        функция = ФункцияМедведь(узел, окружение)
        окружение.присвоить(узел.имя, функция)
        return функция
    
    def посетить_узельвозврат(self, узел, окружение):
        значение = self.посетить(узел.значение, окружение) if узел.значение else None
        raise ВозвратИзИсключения(значение)
    
    def посетить_узелькласс(self, узел, окружение):
        pass  # Упрощенно
    
    def посетить_узельимпорт(self, узел, окружение):
        raise ОшибкаВремениВыполнения(f"Импорт '{узел.модуль}' не реализован")
