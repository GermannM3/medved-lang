#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
Медведь — полноценный язык программирования на кириллице
Точка входа в интерпретатор
"""

имя_системы = "Медведь"
версия = "0.1.0"

import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).parent))

from лексер import Лексер
from парсер import Парсер
from интерпретатор import Интерпретатор
from ошибки import ОшибкаМедведя


справка_текст = f"""
{имя_системы} версии {версия} — язык программирования на кириллице

Использование:
    медведь <файл.мед> [аргументы]
    медведь --помощь
    медведь --версия

Примеры:
    медведь привет.мед
    медведь калькулятор.мед 10 20
"""


def главная_функция():
    """Точка входа в интерпретатор Медведь"""
    
    # Обработка аргументов командной строки
    if len(sys.argv) < 2:
        print(справка_текст)
        sys.exit(0)
    
    первый_арг = sys.argv[1]
    
    if первый_арг in ["--помощь", "-п"]:
        print(справка_текст)
        sys.exit(0)
    
    if первый_арг in ["--версия", "-в"]:
        print(f"{имя_системы} {версия}")
        sys.exit(0)
    
    # Проверка расширения файла
    путь_файла = Path(первый_арг)
    if not путь_файла.exists():
        print(f"Ошибка: Файл не найден: {первый_арг}", file=sys.stderr)
        sys.exit(1)
    
    if путь_файла.suffix != ".мед":
        print(f"Ошибка: Неверное расширение файла. Ожидается .мед, получено: {путь_файла.suffix}", file=sys.stderr)
        sys.exit(1)
    
    # Чтение исходного кода
    try:
        with путь_файла.open("r", encoding="utf-8") as файл:
            исходный_код = файл.read()
    except Exception as е:
        print(f"Ошибка: Не удалось прочитать файл: {е}", file=sys.stderr)
        sys.exit(1)
    
    # Лексический анализ
    try:
        лексер = Лексер(исходный_код, имя_файла=первый_арг)
        токены = лексер.токенизировать()
    except ОшибкаМедведя as е:
        print(е, file=sys.stderr)
        sys.exit(1)
    
    # Синтаксический анализ
    try:
        парсер = Парсер(токены)
        аст = парсер.разобрать()
    except ОшибкаМедведя as е:
        print(е, file=sys.stderr)
        sys.exit(1)
    
    # Выполнение программы
    try:
        интерпретатор = Интерпретатор()
        аргументы = sys.argv[2:] if len(sys.argv) > 2 else []
        интерпретатор.выполнить(аст, аргументы)
    except ОшибкаМедведя as е:
        print(е, file=sys.stderr)
        sys.exit(1)
    except Exception as е:
        print(f"Внутренняя ошибка интерпретатора: {е}", file=sys.stderr)
        sys.exit(1)


if __name__ == "__main__":
    главная_функция()
