# src/main.py

def компилировать_код(код: str):
    """
    Компиляция кода на языке Медведь.
    В этой версии это просто заглушка для тестирования.
    """
    print(f"Компиляция: {код}")

def интерпретировать_код(код: str):
    """
    Интерпретатор для выполнения кода на языке Медведь.
    """
    print(f"Исполнение: {код}")

def главная():
    код = """
    функция приветствие():
        вывод("Привет, Медведь!")
    приветствие()
    """
    компилировать_код(код)  # Для теста компиляции
    интерпретировать_код(код)  # Для теста исполнения

if __name__ == "__главный__":
    главная()

