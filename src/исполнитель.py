#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""Исполнитель для языка Медведь"""

from парсер import (УзелAST, УзелПрограмма, УзелФункция, УзелВозврат, УзелЕсли,
    УзелПока, УзелДля, УзелПечать, УзелПрисваивание, УзелЧисло, УзелСтрока,
    УзелИстина, УзелЛожь, УзелНичто, УзелИмя, УзелБинарнаяОперация,
    УзелУнарнаяОперация, УзелВызовФункции, УзелСписок, УзелИндекс)

class ОшибкаВыполнения(Exception):
    def __init__(self, сообщение): super().__init__(f"Ошибка выполнения: {сообщение}")

class ВозвратИзФункции(Exception):
    def __init__(self, значение): self.значение = значение

class Исполнитель:
    def __init__(self):
        self.локальные = {}
        self.функции = {}
        self.встроенные = {
            'печать': self._встроенная_печать,
            'ввод': self._встроенная_ввод,
            'длина': self._встроенная_длина,
            'диапазон': self._встроенная_диапазон,
            'число': self._встроенная_число,
            'строка': self._встроенная_строка,
        }
    
    def выполнить(self, узел):
        метод = getattr(self, f'_выполнить_{type(узел).__name__}', None)
        if метод: return метод(узел)
        raise ОшибкаВыполнения(f"Неизвестный тип узла: {type(узел).__name__}")
    
    def _выполнить_УзелПрограмма(self, узел):
        результат = None
        for утв in узел.утверждения: результат = self.выполнить(утв)
        return результат
    
    def _выполнить_УзелФункция(self, узел):
        self.функции[узел.имя] = узел
        return None
    
    def _выполнить_УзелВозврат(self, узел):
        значение = self.выполнить(узел.значение) if узел.значение else None
        raise ВозвратИзФункции(значение)
    
    def _выполнить_УзелЕсли(self, узел):
        условие = self.выполнить(узел.условие)
        if условие:
            for утв in узел.тогда: self.выполнить(утв)
        elif узел.иначе:
            for утв in узел.иначе: self.выполнить(утв)
        return None
    
    def _выполнить_УзелПока(self, узел):
        while self.выполнить(узел.условие):
            for утв in узел.тело: self.выполнить(утв)
        return None
    
    def _выполнить_УзелДля(self, узел):
        итерируемое = self.выполнить(узел.итерируемое)
        for элемент in итерируемое:
            self.локальные[узел.переменная] = элемент
            for утв in узел.тело: self.выполнить(утв)
        return None
    
    def _выполнить_УзелПечать(self, узел):
        значение = self.выполнить(узел.значение)
        print(значение)
        return значение
    
    def _выполнить_УзелПрисваивание(self, узел):
        значение = self.выполнить(узел.значение)
        self.локальные[узел.имя] = значение
        return значение
    
    def _выполнить_УзелЧисло(self, узел): return узел.значение
    def _выполнить_УзелСтрока(self, узел): return узел.значение
    def _выполнить_УзелИстина(self, узел): return True
    def _выполнить_УзелЛожь(self, узел): return False
    def _выполнить_УзелНичто(self, узел): return None
    
    def _выполнить_УзелИмя(self, узел):
        if узел.имя in self.локальные: return self.локальные[узел.имя]
        if узел.имя in self.встроенные: return self.встроенные[узел.имя]
        raise ОшибкаВыполнения(f"Переменная '{узел.имя}' не определена")
    
    def _выполнить_УзелБинарнаяОперация(self, узел):
        лево = self.выполнить(узел.лево)
        право = self.выполнить(узел.право)
        ops = {
            '+': lambda a, b: a + b, '-': lambda a, b: a - b,
            '*': lambda a, b: a * b, '/': lambda a, b: a / b if b != 0 else self._ошибка("Деление на ноль"),
            '==': lambda a, b: a == b, '!=': lambda a, b: a != b,
            '<': lambda a, b: a < b, '>': lambda a, b: a > b,
            '<=': lambda a, b: a <= b, '>=': lambda a, b: a >= b,
            'и': lambda a, b: a and b, 'или': lambda a, b: a or b,
        }
        if узел.оператор in ops: return ops[узел.оператор](лево, право)
        raise ОшибкаВыполнения(f"Неизвестный оператор: {узел.оператор}")
    
    def _выполнить_УзелУнарнаяОперация(self, узел):
        операнд = self.выполнить(узел.операнд)
        if узел.оператор == 'не': return not операнд
        if узел.оператор == '-': return -операнд
        raise ОшибкаВыполнения(f"Неизвестный унарный оператор: {узел.оператор}")
    
    def _выполнить_УзелВызовФункции(self, узел):
        аргументы = [self.выполнить(арг) for арг in узел.аргументы]
        if узел.имя in self.встроенные: return self.встроенные[узел.имя](*аргументы)
        if узел.имя in self.функции:
            функция = self.функции[узел.имя]
            старые = self.локальные.copy()
            for i, параметр in enumerate(функция.параметры):
                self.локальные[параметр] = аргументы[i] if i < len(аргументы) else None
            try:
                for утв in функция.тело: self.выполнить(утв)
                return None
            except ВозвратИзФункции as exc: return exc.значение
            finally: self.локальные = старые
        raise ОшибкаВыполнения(f"Функция '{узел.имя}' не определена")
    
    def _выполнить_УзелСписок(self, узел):
        return [self.выполнить(элемент) for элемент in узел.элементы]
    
    def _выполнить_УзелИндекс(self, узел):
        объект = self.выполнить(узел.объект)
        индекс = self.выполнить(узел.индекс)
        if isinstance(объект, (list, str)) and isinstance(индекс, int) and 0 <= индекс < len(объект):
            return объект[индекс]
        raise ОшибкаВыполнения(f"Неверный индекс: {индекс}")
    
    def _встроенная_печать(self, *args): print(*args); return None
    def _встроенная_ввод(self, подсказка=""): return input(подсказка)
    def _встроенная_длина(self, obj): return len(obj)
    def _встроенная_диапазон(self, *args):
        if len(args) == 1: return list(range(int(args[0])))
        if len(args) == 2: return list(range(int(args[0]), int(args[1])))
        if len(args) == 3: return list(range(int(args[0]), int(args[1]), int(args[2])))
        raise ОшибкаВыполнения("Неверное количество аргументов для диапазон")
    def _встроенная_число(self, val): return float(val)
    def _встроенная_строка(self, val): return str(val)
    def _ошибка(self, msg): raise ОшибкаВыполнения(msg)
