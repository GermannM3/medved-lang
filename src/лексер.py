# -*- coding: utf-8 -*-
"""
Лексер для языка Медведь
Разбивает исходный код на токены
"""

from enum import Enum, auto
from typing import List

class ТипТокена(Enum):
    ФУНКЦИЯ = auto()
    ВОЗВРАТ = auto()
    ЕСЛИ = auto()
    ИНАЧЕ = auto()
    ПОКА = auto()
    ДЛЯ = auto()
    В = auto()
    КЛАСС = auto()
    ПЕЧАТЬ = auto()
    ВВОД = auto()
    ИСТИНА = auto()
    ЛОЖЬ = auto()
    НИЧТО = auto()
    И = auto()
    ИЛИ = auto()
    НЕ = auto()
    ЧИСЛО = auto()
    СТРОКА = auto()
    ИМЯ = auto()
    ПРИСВОИТЬ = auto()
    ПЛЮС = auto()
    МИНУС = auto()
    ЗВЕЗДОЧКА = auto()
    СЛЕШ = auto()
    РАВНО = auto()
    НЕ_РАВНО = auto()
    МЕНЬШЕ = auto()
    БОЛЬШЕ = auto()
    МЕНЬШЕ_ИЛИ = auto()
    БОЛЬШЕ_ИЛИ = auto()
    СКОБКА_ОТКРЫТЬ = auto()
    СКОБКА_ЗАКРЫТЬ = auto()
    КВАДРАТНАЯ_ОТКРЫТЬ = auto()
    КВАДРАТНАЯ_ЗАКРЫТЬ = auto()
    ЗАПЯТАЯ = auto()
    ДВОЕТОЧИЕ = auto()
    ТОЧКА = auto()
    КОНЕЦ_ФАЙЛА = auto()

class Токен:
    def __init__(self, тип, значение, строка, колонка):
        self.тип = тип
        self.значение = значение
        self.строка = строка
        self.колонка = колонка
    
    def __repr__(self):
        return f"Токен({self.тип.name}, '{self.значение}', {self.строка}:{self.колонка})"

class ОшибкаЛексера(Exception):
    def __init__(self, сообщение, строка, колонка):
        super().__init__(f"Ошибка в строке {строка}:{колонка}: {сообщение}")

class Лексер:
    КЛЮЧЕВЫЕ_СЛОВА = {
        'функция': ТипТокена.ФУНКЦИЯ,
        'возврат': ТипТокена.ВОЗВРАТ,
        'если': ТипТокена.ЕСЛИ,
        'иначе': ТипТокена.ИНАЧЕ,
        'пока': ТипТокена.ПОКА,
        'для': ТипТокена.ДЛЯ,
        'в': ТипТокена.В,
        'класс': ТипТокена.КЛАСС,
        'печать': ТипТокена.ПЕЧАТЬ,
        'ввод': ТипТокена.ВВОД,
        'истина': ТипТокена.ИСТИНА,
        'ложь': ТипТокена.ЛОЖЬ,
        'ничто': ТипТокена.НИЧТО,
        'и': ТипТокена.И,
        'или': ТипТокена.ИЛИ,
        'не': ТипТокена.НЕ,
    }
    
    def __init__(self, код):
        self.код = код
        self.позиция = 0
        self.строка = 1
        self.колонка = 1
        self.токены = []
    
    def текущий(self):
        if self.позиция >= len(self.код):
            return '\0'
        return self.код[self.позиция]
    
    def следующий(self):
        if self.позиция + 1 >= len(self.код):
            return '\0'
        return self.код[self.позиция + 1]
    
    def продвинуть(self):
        if self.позиция < len(self.код):
            символ = self.код[self.позиция]
            self.позиция += 1
            if символ == '\n':
                self.строка += 1
                self.колонка = 1
            else:
                self.колонка += 1
    
    def добавить_токен(self, тип, значение):
        self.токены.append(Токен(тип, значение, self.строка, self.колонка))
    
    def токенизировать(self):
        while self.текущий() != '\0':
            while self.текущий().isspace() and self.текущий() != '\n':
                self.продвинуть()
            
            if self.текущий() == '#':
                while self.текущий() != '\n' and self.текущий() != '\0':
                    self.продвинуть()
                continue
            
            if self.текущий() == '\n':
                self.продвинуть()
                continue
            
            символ = self.текущий()
            
            if символ.isdigit():
                начало = self.позиция
                while self.текущий().isdigit():
                    self.продвинуть()
                if self.текущий() == '.' and self.следующий().isdigit():
                    self.продвинуть()
                    while self.текущий().isdigit():
                        self.продвинуть()
                self.добавить_токен(ТипТокена.ЧИСЛО, self.код[начало:self.позиция])
                continue
            
            if символ in '"\'':
                кавычка = символ
                self.продвинуть()
                начало = self.позиция
                while self.текущий() != кавычка and self.текущий() != '\0':
                    if self.текущий() == '\\':
                        self.продвинуть()
                    self.продвинуть()
                if self.текущий() == '\0':
                    raise ОшибкаЛексера("Незакрытая строка", self.строка, self.колонка)
                self.добавить_токен(ТипТокена.СТРОКА, self.код[начало:self.позиция])
                self.продвинуть()
                continue
            
            if символ.isalpha() or символ == '_':
                начало = self.позиция
                while self.текущий().isalnum() or self.текущий() == '_':
                    self.продвинуть()
                значение = self.код[начало:self.позиция]
                тип = self.КЛЮЧЕВЫЕ_СЛОВА.get(значение, ТипТокена.ИМЯ)
                self.добавить_токен(тип, значение)
                continue
            
            ops = {
                '+': ТипТокена.ПЛЮС,
                '-': ТипТокена.МИНУС,
                '*': ТипТокена.ЗВЕЗДОЧКА,
                '/': ТипТокена.СЛЕШ,
                '(': ТипТокена.СКОБКА_ОТКРЫТЬ,
                ')': ТипТокена.СКОБКА_ЗАКРЫТЬ,
                '[': ТипТокена.КВАДРАТНАЯ_ОТКРЫТЬ,
                ']': ТипТокена.КВАДРАТНАЯ_ЗАКРЫТЬ,
                ',': ТипТокена.ЗАПЯТАЯ,
                ':': ТипТокена.ДВОЕТОЧИЕ,
                '.': ТипТокена.ТОЧКА,
            }
            
            if символ in ops:
                self.добавить_токен(ops[символ], символ)
                self.продвинуть()
                continue
            
            if символ == '=' and self.следующий() == '=':
                self.добавить_токен(ТипТокена.РАВНО, '==')
                self.продвинуть()
                self.продвинуть()
                continue
            if символ == '=':
                self.добавить_токен(ТипТокена.ПРИСВОИТЬ, '=')
                self.продвинуть()
                continue
            if символ == '!' and self.следующий() == '=':
                self.добавить_токен(ТипТокена.НЕ_РАВНО, '!=')
                self.продвинуть()
                self.продвинуть()
                continue
            if символ == '<' and self.следующий() == '=':
                self.добавить_токен(ТипТокена.МЕНЬШЕ_ИЛИ, '<=')
                self.продвинуть()
                self.продвинуть()
                continue
            if символ == '<':
                self.добавить_токен(ТипТокена.МЕНЬШЕ, '<')
                self.продвинуть()
                continue
            if символ == '>' and self.следующий() == '=':
                self.добавить_токен(ТипТокена.БОЛЬШЕ_ИЛИ, '>=')
                self.продвинуть()
                self.продвинуть()
                continue
            if символ == '>':
                self.добавить_токен(ТипТокена.БОЛЬШЕ, '>')
                self.продвинуть()
                continue
            
            raise ОшибкаЛексера(f"Неизвестный символ: {символ}", self.строка, self.колонка)
        
        self.добавить_токен(ТипТокена.КОНЕЦ_ФАЙЛА, '')
        return self.токены
